class Products {
  final String name;
  final bool isFavorite;
  final String price;
  final int rate;
  final String discount;
  final String image;
  final String type;

  Products({
    required this.name,
    required this.isFavorite,
    required this.price,
    required this.rate,
    required this.discount,
    required this.image,
    required this.type,
  });
}
